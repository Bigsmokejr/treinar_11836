public class OperadoresIncremento {
	public static void main(String[] args) {
		int i = 0;
		System.out.println("Valor de 1 e: " + i++);
		i = i++;
		System.out.println("Valor de 2 e: " + i++);
		i = i++;
		System.out.println("Valor de 3 e: " + i++);
		
	}
}
